import mongoose, { model, Schema } from "mongoose";

const exerciseSchema = new Schema({
    userId: {
        type: Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    username: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    duration: {
        type: Number,
        required: true
    },
    date: {
        type: String
    }
})

export const Exercise = model("Exercise", exerciseSchema)